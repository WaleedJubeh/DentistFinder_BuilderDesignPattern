    private void speciForm_confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_speciForm_confirmActionPerformed
            Builder builder;
           if(spealizeForm_isSpecialCheckBox.isSelected())
           {
               builder  = new DentistSpecialistBuilder();
               builder.setSpecializationsList(specList);
           }else
               builder=new DentistNonSpecialistBuilder();
           
            builder.setEduList(preEduList);
            builder.setAdressList(preAddList);
            builder.setFirstName(preData.get(0));
            builder.setLasttName(preData.get(1));
            builder.setEmail(preData.get(2));
            builder.setWebsite(preData.get(3));
            builder.setPhoneNumber(preData.get(4));
            builder.setGender(preData.get(5));
            builder.setLCN(preData.get(6));
            builder.setlicenseNumber(preData.get(7));
            builder.setlearsinPractice(preData.get(8));
            
            new PreviewForm(builder.getResult());
        // TODO add your handling code here:
    }//GEN-LAST:event_speciForm_confirmActionPerformed

