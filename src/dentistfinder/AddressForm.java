    private void addForm_addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addForm_addBtnActionPerformed

        listModel.addElement((addForm_country.getText()+ " - "+addForm_city.getText()));
        addList.add(new Address(addForm_country.getText(),addForm_state.getText(), addForm_city.getText(), addForm_postalCode.getText()));
        addForm_list.setModel(listModel);
        clearFields();

    }//GEN-LAST:event_addForm_addBtnActionPerformed

