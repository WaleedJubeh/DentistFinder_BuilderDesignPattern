    private void addForm_nextBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addForm_nextBtnActionPerformed
        
        if(nextForm==null)
            nextForm=new SpecializationForm(personalData, eduList, addList, this);
        else
        {
            nextForm.setPreData(personalData);
            nextForm.setPreEduList(eduList);
            nextForm.setPreAddList(addList);
        }
        nextForm.setVisible(true);
        this.setVisible(false);

        // TODO add your handling code here:
    }//GEN-LAST:event_addForm_nextBtnActionPerformed

